package day2;

public class Loop {
    public static void main(String[] args) {
        // 반복문 -> 첫번째 벽ㅋㅋ

        // while, for
        // while
        int i = 0;

        while(i<10){
            System.out.println("안녕?");
            i++;
        }

        for (int j = 0; j < 10 ; j++) {
            System.out.println("Hi");
        }

    }
}
